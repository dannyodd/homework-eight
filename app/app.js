function initListeners() {
    $("nav a").click(function(e){
        // This stores the id from the anchor tag
        let aID = e.currentTarget.id;
        let contentID = aID + "Content";  
        let heroImg = aID + "-hero.jpg";
        
        
        $("#app").html(eval(contentID));
        $("#hero").css('background-image', 
        'linear-gradient(rgba(0,0,0,0.5), rgba(0,0,0,0.5)), url(../images/hero/' + heroImg + ')');
    });
}


$(document).ready(function() {
    initListeners();
    $("#app").html(homeContent);
});